# test.S - 我们的第一个 RISC-V 汇编程序

.global _start      # 告诉编译器这是程序的入口点

.text               # 告诉编译器下面是代码段
_start:
    # 让我们算一个复杂点的：(10 + 20) - 5 = 25
    addi x1, x0, 10 # 把 10 存进 x1 (x1 = 0 + 10)
    addi x2, x0, 20 # 把 20 存进 x2 (x2 = 0 + 20)
    add  x3, x1, x2 # x3 = x1 + x2 (此时 x3 是 30)
    addi x4, x0, 5  # 把 5 存进 x4
    sub  x5, x3, x4 # x5 = x3 - x4 (x5 = 30 - 5 = 25)

    # 程序结束，进入死循环，防止跑飞
end:
    j end           # 跳转到 end 标签（注意：这个指令你目前还没实现，它会报错停机，正好当作结束标志）